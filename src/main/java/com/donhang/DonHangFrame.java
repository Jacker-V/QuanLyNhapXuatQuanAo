    public void loadAllDataIntoTable(List<DonHangBean> list){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        
        for(DonHangBean bean : list) {
            Vector v = new Vector();
            v.add(bean.getIddonhang());
            v.add(bean.getTenSanPham());
            v.add(bean.getKhachHang());
            v.add(bean.getGia());
            v.add(bean.getNgayXuat());
            v.add(bean.getSize());
            v.add(bean.getMau());
            v.add(bean.getSoLuong());
            
            dtm.addRow(v);
        
        }
        
    
    }
    
    public void loadTable(){
        DonHangBal bal = new DonHangBal();
        List list = bal.loadData();
        loadAllDataIntoTable(list);
    
    }

