        int SoLuong = Integer.parseInt(jTextFieldSoLuong.getText());
        
        
        XuatBean bean = new XuatBean(MaHangHoa, "TenHangHoa", "LoaiHangHoa", "XuatXu", "NgaySanXuat", TOP_ALIGNMENT, SoLuong);
        XuatBal bal = new XuatBal();
