    public void loadAllDataIntoTable(List<XuatBean> list){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        
        for(XuatBean bean : list) {
            Vector v = new Vector();
            v.add(bean.getIdsanpham());
            v.add(bean.getTenSanPham());
            v.add(bean.getNhaCungCap());
            v.add(bean.getGiaNhap());
            v.add(bean.getNgayNhap());
            v.add(bean.getSize());
            v.add(bean.getMau());
            v.add(bean.getSoLuong());
            
            dtm.addRow(v);
        
        }
        
    
    }
    
    public void loadTable(){
        XuatBal bal = new XuatBal();
        List list = bal.loadData();
        loadAllDataIntoTable(list);
    
    }

